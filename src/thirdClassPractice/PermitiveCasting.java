package thirdClassPractice;

public class PermitiveCasting {

	public static void main(String[] args) {

		// Primitive Casting 
		double b =12/7;
		System.out.println(b); //Widening 
		double b2 =10;
		System.out.println(b2);//Widening 
		
		int i=(int)2.99;
		System.out.println(i); //Narrowing
		byte b1=(byte)150;
		System.out.println(b1); //Narrowing
		
		
		
	}

}
