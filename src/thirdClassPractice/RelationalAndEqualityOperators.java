package thirdClassPractice;

public class RelationalAndEqualityOperators {

	public static void main(String[] args) {

	int num=10;
	int num1=15;
	System.out.println(num>num1);
	System.out.println(num>=num1);
	System.out.println(num<num1);
	System.out.println(num==num1);
	System.out.println(num!=num1);
	
	int x=100;
	int y=150;
	System.out.println(x==y);
	System.out.println(x>y);
	System.out.println(x<y);
	System.out.println(x!=y);
	System.out.println(x=y);
	
	boolean result=x>y;
	System.out.println(result);
	
	result=x==y;
	System.out.println(result);
	
	System.out.println(x==y);
	System.out.println(x>y);
	System.out.println(x=y);
	System.out.println(x);
	
	
	
		
	}

}
