package com.syntax.class02;

public class VariableDecoration {

	public static void main(String[] args) {
		
		//  1 way to create the variable 
		// create variable =assigning value
		//declare a variable=initializing 
		double $num=12.99;
	//Declaration Variable // Assigning the value or initialize 
		
		
		// 2 way to create variable 
			//declare a variable 
			//initialize or assigne value
		
		int myBox; // we declare variable only once dont double it  
	    myBox=135; //assigning the value
	    System.out.println(myBox);
	    
	    myBox=155; //assigning the value 
	    System.out.println(myBox);
		
		char myStar; 
		myStar='b';
		System.out.println(myStar);
		
		myStar='*';
		System.out.println(myStar);
		
		myStar='&';
		System.out.println(myStar);
		
		// 3 way to create variable 
		
		int num1, num2, num3; // if variable is of same type we can declare all one line
		num1=10;
		num2=20;
		num3=30;
		System.out.println(num3);
		
		short type1=50;
		short type2=60;
		
		short type3=type1;
		System.out.println(type3);
		
		
		char char1='C';
		char char2='b';
		
		char char3=char1;
		System.out.println(char3);
				

		
		
	}

}
