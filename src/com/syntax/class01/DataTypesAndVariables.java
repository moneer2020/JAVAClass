package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {

		byte JavaBooks=120;
		System.out.println(JavaBooks);
		
		short EclipsBooks=500;
		System.out.println(EclipsBooks);
		
		int EnglishBooks=50000;
		System.out.println(EnglishBooks);
		
		long SpidermanDVD=5000000000000000L;
		System.out.println(SpidermanDVD);
		
		float ApplePrice=5.99f;
		System.out.println(ApplePrice);
		
		double StrawberryPrice=10.888;
		System.out.println(StrawberryPrice);
		
		boolean FourWheelDrive=true;
		System.out.println(FourWheelDrive);
		
		boolean FiveWheelDrive=false;
		System.out.println(FiveWheelDrive);
		
		char MyInitial='F';
		System.out.println(MyInitial);
		
		int num=10;
		int num1=11;
		num1=11;
		num1=12;
		
		String str="Hello";
		String str1="Friends";
		System.out.println(str+" "+str1);
		
	}

}
